const add = (i,j)=>{
    return i + j;
}
const subtract = (i,j)=>{
    return i - j;
}
const multiply = (i,j)=>{
    return i * j;
}
const division = (i,j)=>{
    return i /j;
}
const square = (i)=>{
    return i * i;
}

module.exports={
    add,
    subtract,
    multiply,
    division,
    square
}